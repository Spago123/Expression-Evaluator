package ba.unsa.etf.rpr.Mathematics.TwoArgumentOperations;

/**
 * Class that does the sum of two numbers
 */
public class Sumation extends TwoArgumentOperations {
    /**
     * Method that calculates the sum of two numbers
     * @param num1 double
     * @param num2 double
     * @return double
     */
    @Override
    public double doCalculation(double num1, double num2) {
        return num1 + num2;
    }

}
